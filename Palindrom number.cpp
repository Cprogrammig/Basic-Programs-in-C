/*Palindrom   number  or not*/
#include<stdio.h>
int main()
{
	int n,r,a,rev=0;
	printf("Enter the number:");
	scanf("%d",&n);
	a=n;
	while(n!=0)
	{
		r=n%10;
		n=n/10;
		rev=rev*10+r;
	}
	if(rev==a)
	printf("%d is palindrom number",a);
	else
	printf("%d is not a palindrom number",a);
	return 0;
}
