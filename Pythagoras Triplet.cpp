/*Pythagoras Triplet or not*/
#include<stdio.h>
int main()
{
	int a,b,c;
	printf("Enter a , b, c values :");
	scanf("%d %d %d",&a,&b,&c);
	if((a*a)+(b*b)==(c*c))
	printf("%d ,%d ,%d form a pythagoras triplet",a,b,c);
	else
	printf("%d ,%d ,%d does not  form a pythagoras triplet",a,b,c);
	return 0;
}
